class Music {
  final String id;
  final String title;
  final String artist;
  final String image;
  final String file;
  final String duration;
  final String genre;
  final String year;
  final String url;

  Music({
    required this.id,
    required this.title,
    required this.artist,
    required this.image,
    required this.file,
    required this.duration,
    required this.genre,
    required this.year,
    required this.url,
  });
}
